    "- `price` is **TEXT**, not REAL — always `CAST(price AS REAL)` for numeric ops.",
    "- `genre` uses Chinese category names. For fuzzy search use `LIKE '%技术%'`.",
    "'''",
    "",
    "print(books_skill)",
