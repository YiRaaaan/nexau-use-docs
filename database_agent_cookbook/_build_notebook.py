    '| **Example queries** | Few-shot 示例 SQL | 模型会"照着写" |',
    "| **Gotchas** | 陷阱和注意事项 | **最有价值的部分**——直接减少错误 |",
    "",
    '> **Gotchas 是最有价值的部分。** 模型犯错通常不是因为"不知道有这张表"，',
    '> 而是因为不知道 `price` 是 TEXT、不知道"已取消"的订单需要排除。',
